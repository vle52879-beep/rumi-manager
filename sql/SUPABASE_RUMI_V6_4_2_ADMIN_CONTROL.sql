-- 2) Xóa mềm lịch sử lấy nguyên liệu (không hoàn tồn kho).
-- 3) Giới hạn tuyệt đối 56 giờ/tuần.
-- 4) Đổi nhân viên cho ca đã xếp, chỉ sang người đã đăng ký đúng ca.
