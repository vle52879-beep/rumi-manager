create or replace function public.rumi_auth_register_failure(
  p_key_hash text,
  p_user_id bigint default null,
  p_now timestamptz default now()
)
returns jsonb
language plpgsql
security definer
set search_path = public
as $$
declare
  v_row public.rumi_login_throttles%rowtype;
  v_attempts integer;
  v_lock_minutes integer := 0;
begin
  insert into public.rumi_login_throttles(key_hash, attempts, first_attempt_at, last_attempt_at)
  values (p_key_hash, 1, p_now, p_now)
  on conflict (key_hash) do update set
    attempts = case
      when public.rumi_login_throttles.first_attempt_at < p_now - interval '10 minutes' then 1
      else public.rumi_login_throttles.attempts + 1
    end,
    first_attempt_at = case
      when public.rumi_login_throttles.first_attempt_at < p_now - interval '10 minutes' then p_now
      else public.rumi_login_throttles.first_attempt_at
    end,
    last_attempt_at = p_now,
    locked_until = case
      when (case when public.rumi_login_throttles.first_attempt_at < p_now - interval '10 minutes' then 1 else public.rumi_login_throttles.attempts + 1 end) >= 10 then p_now + interval '60 minutes'
      when (case when public.rumi_login_throttles.first_attempt_at < p_now - interval '10 minutes' then 1 else public.rumi_login_throttles.attempts + 1 end) >= 5 then p_now + interval '15 minutes'
      else null
    end
  returning * into v_row;

  v_attempts := v_row.attempts;
  if v_row.locked_until is not null then
    v_lock_minutes := greatest(1, ceil(extract(epoch from (v_row.locked_until - p_now)) / 60)::integer);
  end if;

  if p_user_id is not null then
    update public.rumi_users
    set failed_login_count = least(failed_login_count + 1, 1000),
        last_failed_login_at = p_now,
        locked_until = case
          when v_attempts >= 10 then p_now + interval '60 minutes'
          when v_attempts >= 5 then p_now + interval '15 minutes'
          else locked_until
        end
    where id = p_user_id;
  end if;

  return jsonb_build_object(
    'attempts', v_attempts,
    'locked', v_row.locked_until is not null and v_row.locked_until > p_now,
    'locked_until', v_row.locked_until,
    'retry_after_minutes', v_lock_minutes
  );
end;
$$;

create or replace function public.rumi_auth_clear_failures(
  p_key_hash text,
  p_user_id bigint default null,
  p_now timestamptz default now()
)
returns void
language plpgsql
security definer
set search_path = public
as $$
begin
  delete from public.rumi_login_throttles where key_hash = p_key_hash;
  if p_user_id is not null then
    update public.rumi_users
    set failed_login_count = 0,
        locked_until = null,
        last_failed_login_at = null
    where id = p_user_id;
  end if;
end;
$$;

