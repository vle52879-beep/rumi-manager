declare t text;
begin
  foreach t in array array[
    'rumi_employees','rumi_shifts','rumi_attendance','rumi_inventory','rumi_withdrawals',
    'rumi_purchase_requests','rumi_payroll_adjustments','rumi_payroll_payments','rumi_users',
